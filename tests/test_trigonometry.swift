func main() {

    // Arrays
    let xf:[Float] = [1.0, 2.0, 3.0, 3.14159265]
    let xd:[Double] = [1.0, 2.0, 3.0, 3.14159265358979]
    print("xf: \(xf)")
    print("xd: \(xd)")

    // Testing trigonometry.
    print("cos(xf): \(cos(xf))")
    print("cos(xd): \(cos(xd))")
    print("tan(xf): \(tan(xf))")
    print("tan(xd): \(tan(xd))")
    print("cot(xf): \(cot(xf))")
    print("cot(xd): \(cot(xd))")
    print("csc(xf): \(csc(xf))")
    print("csc(xd): \(csc(xd))")
}

main()